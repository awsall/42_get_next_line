#include <unistd.h>

void	ft_first_word(char *str)
{
	int	i;

	i = 0;
	if (!str)
		return;
	while (str[i] && str[i] <= 32)
		i++;
	while (str[i] != ' ' && str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
}
